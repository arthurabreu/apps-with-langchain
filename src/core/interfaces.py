"""
Abstract interfaces and base classes for the LangChain application.

For Android Developers:
- Think of these as Kotlin Interfaces or Abstract Classes.
- Protocols (ITokenCounter, etc.) are like Kotlin interfaces that use structural typing (similar to 'interface' but more flexible).
- ABC (ILanguageModel) is exactly like a Kotlin 'abstract class'.
- @dataclass (ModelConfig) is exactly like a Kotlin 'data class'.
- Any is like Kotlin's 'Any?'.
- Optional[T] is like Kotlin's 'T?'.
"""

from abc import ABC, abstractmethod
from typing import Optional, Dict, Any, Protocol
from dataclasses import dataclass
from enum import Enum


class GenerationStrategy(Enum):
    """
    Strategies for text generation.
    Analogous to a Kotlin 'enum class'.
    """
    STANDARD = "standard"
    STREAMING = "streaming"


@dataclass
class ModelConfig:
    """
    Configuration for language models.
    
    Android Analogy: Kotlin 'data class'. It automatically generates 
    equals(), hashCode(), and toString().
    
    Attributes:
        model_name: The identifier of the model (e.g., 'gpt-4').
        temperature: Controls randomness (0.0 to 1.0).
        max_tokens: Limit on generated output length.
        api_key: Secret key for the provider.
        system_message: Initial context/instructions for the AI.
        generation_strategy: How to receive the output (standard or stream).
        additional_params: Map of extra configurations.
    """
    model_name: str
    temperature: float = 0.2
    max_tokens: int = 512
    api_key: Optional[str] = None
    system_message: Optional[str] = None
    generation_strategy: GenerationStrategy = GenerationStrategy.STANDARD
    additional_params: Optional[Dict[str, Any]] = None


@dataclass
class GenerationResult:
    """
    Result of a text generation operation.
    
    Android Analogy: A Response DTO (Data Transfer Object).
    
    Attributes:
        content: The actual text generated by the AI.
        tokens_used: Cost-related metric.
        cost: Estimated USD cost of the call.
        metadata: Extra info from the provider.
        strategy_used: Which strategy was used for this result.
    """
    content: str
    tokens_used: Optional[int] = None
    cost: Optional[float] = None
    metadata: Optional[Dict[str, Any]] = None
    strategy_used: Optional[GenerationStrategy] = None


