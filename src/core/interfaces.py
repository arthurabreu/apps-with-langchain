class ILanguageModel(ABC):
    """
    Abstract base class for all LLM implementations.
    
    Android Analogy: 'abstract class ILanguageModel(val config: ModelConfig)'.
    
    Note: Python uses '__init__' for constructors.
    """
    
    def __init__(self, config: ModelConfig):
        """
        Constructor.
        
        Args:
            config: The model configuration data class.
        """
        self.config = config
        self._validate_config()
    
    @abstractmethod
    def _validate_config(self) -> None:
        """
        Validate the configuration. 
        Must be implemented by subclasses.
        """
        pass
    
    @abstractmethod
    def generate(self, prompt: str, **kwargs) -> GenerationResult:
        """
        Main generation entry point.
        
        **kwargs: Analogous to passing a Map or optional parameters in Kotlin.
        """
        pass
    
    @abstractmethod
    def get_model_info(self) -> Dict[str, Any]:
        """Return provider-specific metadata."""
        pass
    
    @property
    @abstractmethod
    def provider(self) -> str:
        """
        Read-only property for the provider name.
        Like a Kotlin 'val provider: String'.
        """
        pass


